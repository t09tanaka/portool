//! Pure rendering of the `.env.portool` file contents (spec §7.1).

use crate::manifest::{env_var_name, Manifest};
use std::fmt::Write as _;

/// Renders the `.env.portool` contents for a worktree.
///
/// The header is always the two-line comment block naming the block,
/// project, and worktree path, followed by the two stable identity lines
/// (`PORTOOL_PROJECT_ID=` / `PORTOOL_WORKTREE_ID=`), which are emitted
/// whether or not a manifest exists. When `manifest` is `Some`, one
/// `<KEY>_PORT=<port>` line is emitted per declared port, in the manifest's
/// (offset-ascending) order. When `manifest` is `None`, a single
/// `PORT=<block_start>` line is emitted instead.
pub fn render(
    project_name: &str,
    worktree_path: &str,
    block: (u16, u16),
    manifest: Option<&Manifest>,
    project_id: &str,
    worktree_id: &str,
) -> String {
    let mut out = String::new();
    let _ = writeln!(out, "# generated by portool \u{2014} DO NOT EDIT");
    let _ = writeln!(
        out,
        "# block: {}-{}  project: {}  worktree: {}",
        block.0, block.1, project_name, worktree_path
    );
    for (name, value) in variables(block, manifest, project_id, worktree_id) {
        let _ = writeln!(out, "{name}={value}");
    }

    out
}

/// The portool-managed variables for a worktree, in `.env.portool` order:
/// the two identity lines, then one `<KEY>_PORT` per manifest port (or a
/// single `PORT` when there is no manifest).
pub fn variables(
    block: (u16, u16),
    manifest: Option<&Manifest>,
    project_id: &str,
    worktree_id: &str,
) -> Vec<(String, String)> {
    let mut vars = Vec::new();
    vars.push(("PORTOOL_PROJECT_ID".to_string(), project_id.to_string()));
    vars.push(("PORTOOL_WORKTREE_ID".to_string(), worktree_id.to_string()));

    match manifest {
        Some(m) => {
            for (key, offset) in &m.ports {
                let port = block.0 + offset;
                vars.push((env_var_name(key), port.to_string()));
            }
        }
        None => {
            vars.push(("PORT".to_string(), block.0.to_string()));
        }
    }

    vars
}

#[cfg(test)]
mod tests {
    use super::*;
    use crate::manifest::Manifest;

    const PROJECT_ID: &str = "abe4c983b7295f37";
    const WORKTREE_ID: &str = "740da24128c5e2f7";

    #[test]
    fn renders_manifest_exact_bytes() {
        let manifest = Manifest::parse("[ports]\nweb = 0\napi = 1\nhmr = 2\ndb = 3\n").unwrap();
        let rendered = render(
            "myapp",
            "/home/user/dev/myapp",
            (3000, 3004),
            Some(&manifest),
            PROJECT_ID,
            WORKTREE_ID,
        );

        let expected = "# generated by portool \u{2014} DO NOT EDIT\n\
# block: 3000-3004  project: myapp  worktree: /home/user/dev/myapp\n\
PORTOOL_PROJECT_ID=abe4c983b7295f37\n\
PORTOOL_WORKTREE_ID=740da24128c5e2f7\n\
WEB_PORT=3000\n\
API_PORT=3001\n\
HMR_PORT=3002\n\
DB_PORT=3003\n";

        assert_eq!(rendered, expected);
    }

    #[test]
    fn renders_default_port_line_when_no_manifest() {
        let rendered = render(
            "myapp",
            "/home/user/dev/myapp",
            (3000, 3004),
            None,
            PROJECT_ID,
            WORKTREE_ID,
        );

        let expected = "# generated by portool \u{2014} DO NOT EDIT\n\
# block: 3000-3004  project: myapp  worktree: /home/user/dev/myapp\n\
PORTOOL_PROJECT_ID=abe4c983b7295f37\n\
PORTOOL_WORKTREE_ID=740da24128c5e2f7\n\
PORT=3000\n";

        assert_eq!(rendered, expected);
    }

    #[test]
    fn id_lines_precede_port_variables() {
        let manifest = Manifest::parse("[ports]\nweb = 0\n").unwrap();
        let rendered = render(
            "p",
            "/w",
            (3000, 3004),
            Some(&manifest),
            PROJECT_ID,
            WORKTREE_ID,
        );

        let project_id_pos = rendered.find("PORTOOL_PROJECT_ID=").unwrap();
        let worktree_id_pos = rendered.find("PORTOOL_WORKTREE_ID=").unwrap();
        let port_pos = rendered.find("WEB_PORT=").unwrap();
        assert!(project_id_pos < worktree_id_pos);
        assert!(worktree_id_pos < port_pos);
    }

    #[test]
    fn variables_match_render_body_with_manifest() {
        let manifest = Manifest::parse("[ports]\nweb = 0\napi = 1\nhmr = 2\ndb = 3\n").unwrap();
        let block = (3000, 3004);

        let vars = variables(block, Some(&manifest), PROJECT_ID, WORKTREE_ID);
        assert_eq!(
            vars,
            vec![
                ("PORTOOL_PROJECT_ID".to_string(), PROJECT_ID.to_string()),
                ("PORTOOL_WORKTREE_ID".to_string(), WORKTREE_ID.to_string()),
                ("WEB_PORT".to_string(), "3000".to_string()),
                ("API_PORT".to_string(), "3001".to_string()),
                ("HMR_PORT".to_string(), "3002".to_string()),
                ("DB_PORT".to_string(), "3003".to_string()),
            ]
        );

        // render() must be exactly the two comment lines plus these
        // variables, so the two can never drift apart.
        let rendered = render(
            "myapp",
            "/home/user/dev/myapp",
            block,
            Some(&manifest),
            PROJECT_ID,
            WORKTREE_ID,
        );
        let body: Vec<(String, String)> = rendered
            .lines()
            .filter(|line| !line.starts_with('#'))
            .map(|line| {
                let (name, value) = line.split_once('=').unwrap();
                (name.to_string(), value.to_string())
            })
            .collect();
        assert_eq!(body, vars);
    }

    #[test]
    fn variables_use_single_port_line_when_no_manifest() {
        let vars = variables((3000, 3004), None, PROJECT_ID, WORKTREE_ID);
        assert_eq!(
            vars,
            vec![
                ("PORTOOL_PROJECT_ID".to_string(), PROJECT_ID.to_string()),
                ("PORTOOL_WORKTREE_ID".to_string(), WORKTREE_ID.to_string()),
                ("PORT".to_string(), "3000".to_string()),
            ]
        );
    }

    #[test]
    fn ends_with_trailing_newline() {
        let rendered = render("p", "/w", (3000, 3004), None, PROJECT_ID, WORKTREE_ID);
        assert!(rendered.ends_with('\n'));
    }
}
